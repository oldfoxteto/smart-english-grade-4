// Search vocabulary
export const searchVocabulary = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return allVocabulary.filter(word => 
    word.english.toLowerCase().includes(lowercaseQuery) ||
    word.arabic.includes(query)
  );
};
